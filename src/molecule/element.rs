use phf::phf_map;

use crate::error::RuatomError;

#[derive(PartialEq, Eq, Hash)]
pub enum Specification {
    DayLight,
    General,
    OpenSMILES,
}

#[derive(Clone, Debug)]
pub struct Element {
    atomic_number: u8,
    symbol: &'static str,
    valence: [u8; 3],
    mass: f64,
    isotope_mass: f64,
}
impl Element {
    pub const fn new(
        symbol: &'static str,
        atomic_number: u8,
        valence: [u8; 3],
        mass: f64,
        isotope_mass: f64,
    ) -> Self {
        Self {
            atomic_number,
            symbol,
            valence,
            mass,
            isotope_mass,
        }
    }

    pub fn read(c: &str) -> Option<Self> {
        ELEMENT_MAP.get(c).map_or(None, |e| Some(e.clone()))
    }

    pub fn atomic_number(&self) -> u8 {
        self.atomic_number
    }

    pub fn symbol(&self) -> &str {
        self.symbol
    }

    pub(crate) fn implict_hydrogen_amount(&self, valence: u8) -> u8 {
        self.valence
            .iter()
            .find(|&&i| i >= valence)
            .map_or(0, |v| v - valence)
    }

    pub(crate) fn implict_atom_hydrogen(&self, valence: u8) -> u8 {
        if self.valence[0] - valence > 0 {
            return self.valence[0] - valence;
        } else {
            return 0;
        }
    }

    pub fn is_aromatic(&self, spec: Specification) -> bool {
        match spec {
            Specification::DayLight => DAYLIGHT_AROMATIC_ELEMENT.contains(&self.atomic_number),
            Specification::General => GENERAL_AROMATIC_ELEMENT.contains(&self.atomic_number),
            Specification::OpenSMILES => OPENSMILE_AROMATIC_ELEMENT.contains(&self.atomic_number),
        }
    }

    #[inline]
    pub fn get_mass(&self) -> f64 {
        self.mass
    }

    #[inline]
    pub fn get_exact_mass(&self, isotope: i16) -> Result<f64, RuatomError> {
        if isotope > 0 {
            let k = format!("{}_{}", self.symbol, isotope);
            let mass = ISOTOPES_ATOM_MASS
                .get(&k)
                .ok_or(RuatomError::IsotopeError(self.symbol, isotope))?;
            return Ok(*mass);
        }
        Ok(self.isotope_mass)
    }
}

pub fn valid_element_symbol(symbol: &str) -> bool {
    ELEMENT_MAP.contains_key(symbol)
}

